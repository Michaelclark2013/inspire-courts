// Idempotent migration applier — runs every drizzle/*.sql statement and
// swallows the "already exists" / "duplicate column" errors so it can be
// rerun safely on a partially-migrated DB. Used to bring production back
// in sync after schema drift.
//
// Run: node scripts/apply-migrations.mjs
// Reads TURSO_DATABASE_URL + TURSO_AUTH_TOKEN from env.

import { createClient } from "@libsql/client";
import { readFileSync, readdirSync } from "node:fs";
import { join } from "node:path";

const DRIZZLE_DIR = new URL("../drizzle/", import.meta.url).pathname;

const url = process.env.TURSO_DATABASE_URL;
const authToken = process.env.TURSO_AUTH_TOKEN;
if (!url) {
  console.error("TURSO_DATABASE_URL not set");
  process.exit(1);
}
const client = createClient({ url, authToken });

const files = readdirSync(DRIZZLE_DIR)
  .filter((f) => f.endsWith(".sql"))
  .sort();

let applied = 0;
let skipped = 0;
let failed = 0;

for (const file of files) {
  const sql = readFileSync(join(DRIZZLE_DIR, file), "utf8");
  // Migration files use --> statement-breakpoint when generated by
  // drizzle-kit, but a few hand-written migrations omit them and rely
  // on `;` as the separator. libsql rejects multi-statement strings,
  // so we split by both, strip comments, and run statements one at
  // a time.
  const statements = sql
    .split(/-->\s*statement-breakpoint\s*\n?/)
    .flatMap((chunk) => splitOnSemicolons(chunk))
    .map((s) => s.trim())
    .filter(Boolean);

  for (const stmt of statements) {
    try {
      await client.execute(stmt);
      applied++;
    } catch (err) {
      const msg = err.message || "";
      // SQLite error: table X already exists / duplicate column name
      if (
        /already exists/i.test(msg) ||
        /duplicate column/i.test(msg) ||
        /no such table.*\bDROP\b/i.test(msg)
      ) {
        skipped++;
      } else {
        failed++;
        console.error(`FAIL [${file}]:`, msg);
        console.error("  stmt:", stmt.slice(0, 120).replace(/\s+/g, " "));
      }
    }
  }
}

console.log(`\nDone — applied=${applied}, skipped=${skipped}, failed=${failed}`);
process.exit(failed > 0 ? 1 : 0);

// Splits a SQL chunk on `;` boundaries while ignoring semicolons that
// appear inside string literals or line comments.
function splitOnSemicolons(chunk) {
  const out = [];
  let buf = "";
  let inString = false;
  let stringChar = "";
  for (let i = 0; i < chunk.length; i++) {
    const c = chunk[i];
    const next = chunk[i + 1];
    // Strip `-- ...` line comments
    if (!inString && c === "-" && next === "-") {
      while (i < chunk.length && chunk[i] !== "\n") i++;
      continue;
    }
    if (inString) {
      buf += c;
      if (c === stringChar) inString = false;
      continue;
    }
    if (c === "'" || c === '"' || c === "`") {
      inString = true;
      stringChar = c;
      buf += c;
      continue;
    }
    if (c === ";") {
      if (buf.trim()) out.push(buf);
      buf = "";
      continue;
    }
    buf += c;
  }
  if (buf.trim()) out.push(buf);
  return out;
}
